package programs;

public class MathsClass {
	public static void main(String[] args) {
		int absValue = Math.abs(-10);
		System.out.println(absValue); // output: 10
		System.out.println(Math.max(10, 20)); //Output: 20 maximum value
		System.out.println(Math.min(10, 20)); // Output: 10
		System.out.println(Math.pow(2, 3)); // Output: 8
		System.out.println(Math.sqrt(4)); // Output: 2
		System.out.println(Math.ceil(2.1)); // 3.0
		System.out.println(Math.floor(2.9)); // 2.0
		System.out.println(Math.round(2.5)); // 3
		
	}
}
